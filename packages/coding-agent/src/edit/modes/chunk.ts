type ChunkExecutionContext = {
	resolvedPath: string;
	sourceExists: boolean;
	chunkLanguage: string | undefined;
};

interface ExecuteChunkModeOptions {
	session: ToolSession;
	params: ChunkParams;
	signal?: AbortSignal;
	batchRequest?: LspBatchRequest;
	writethrough: WritethroughCallback;
	beginDeferredDiagnosticsForPath: (path: string) => WritethroughDeferredHandle;
}

export function isChunkParams(params: unknown): params is ChunkParams {
	return (
		typeof params === "object" &&
		params !== null &&
		"edits" in params &&
		Array.isArray(params.edits) &&
		params.edits.length > 0 &&
		typeof params.edits[0] === "object" &&
		params.edits[0] !== null &&
		"target" in params.edits[0]
	);
}

function parseChunkTarget(target: string): ParsedChunkTarget {
	return resolveChunkTarget(target);
}

function joinChunkPath(parent: string, child: string): string {
	if (child.length === 0) {
		throw new Error("Sibling name cannot be empty.");
	}
	if (parent.length === 0 || child.startsWith(`${parent}.`)) {
		return child;
	}
	return `${parent}.${child}`;
}

function describeChunkTarget(selector: string): string {
	return selector.length > 0 ? `"${selector}"` : "root";
}

async function resolveRequiredChunkChecksum(params: {
	op: string;
	crc: string | undefined;
	selector: string;
	context: ChunkExecutionContext;
}): Promise<string> {
	const { op, crc, selector, context } = params;
	if (crc) return crc.toUpperCase();

	if (selector.length > 0 && context.sourceExists) {
		const resolved = await getChunkInfoForFile(context.resolvedPath, context.chunkLanguage, selector);
		if (resolved) {
			throw new Error(
				`Checksum required for ${op} on ${describeChunkTarget(selector)}. ` +
					`Re-read the chunk to get its checksum, then pass target: "${selector}#${resolved.checksum}".`,
			);
		}
		throw new Error(`Chunk not found: "${selector}". Re-read the file to see available chunk paths.`);
	}

	throw new Error(
		`Checksum required for ${op} on ${describeChunkTarget(selector)}. ` +
			"Re-read the file first, then pass target with a #XXXX checksum suffix copied from the read output.",
	);
}

async function normalizeChunkEditOperation(
	edit: ChunkToolEdit,
	context: ChunkExecutionContext,
): Promise<ChunkEditOperation> {
	const { selector, crc } = parseChunkTarget(edit.target);
	const op = edit.op ?? "replace";
	const content = edit.content;

	switch (op) {
		case "append":
		case "append_child":
			return { op: "append_child", sel: selector, content };
		case "prepend":
		case "prepend_child":
			return { op: "prepend_child", sel: selector, content };
		case "after":
		case "append_sibling":
			return {
				op: "append_sibling",
				sel: resolveChunkSiblingSelector({ selector, anchor: edit.anchor, op }),
				content,
			};
		case "before":
		case "prepend_sibling":
			return {
				op: "prepend_sibling",
				sel: resolveChunkSiblingSelector({ selector, anchor: edit.anchor, op }),
				content,
			};
		case "replace_body":
			return {
				op: "replace_body",
				sel: selector,
				crc: await resolveRequiredChunkChecksum({ op: "replace_body", crc, selector, context }),
				content,
			};
		default:
			return {
				op: "replace",
				sel: selector,
				crc: await resolveRequiredChunkChecksum({ op: "replace", crc, selector, context }),
				content,
			};
	}
}

async function normalizeChunkEditOperations(
	edits: ChunkToolEdit[],
	context: ChunkExecutionContext,
): Promise<ChunkEditOperation[]> {
	const operations: ChunkEditOperation[] = [];
	for (const edit of edits) {
		operations.push(await normalizeChunkEditOperation(edit, context));
	}
	return operations;
}

async function writeChunkResult(params: {
	result: ChunkEditResult;
	resolvedPath: string;
	sourceFile: BunFile;
	sourceText: string;
	sourceExists: boolean;
	signal?: AbortSignal;
	batchRequest?: LspBatchRequest;
	writethrough: WritethroughCallback;
	beginDeferredDiagnosticsForPath: (path: string) => WritethroughDeferredHandle;
}): Promise<AgentToolResult<EditToolDetails, typeof chunkEditParamsSchema>> {
	const {
		result,
		resolvedPath,
		sourceFile,
		sourceText,
		sourceExists,
		signal,
		batchRequest,
		writethrough,
		beginDeferredDiagnosticsForPath,
	} = params;

	const { bom, text } = stripBom(sourceText);
	const originalEnding = detectLineEnding(text);
	const finalContent = bom + restoreLineEndings(result.diffSourceAfter, originalEnding);
	const diagnostics = await writethrough(resolvedPath, finalContent, signal, sourceFile, batchRequest, dst =>
		dst === resolvedPath ? beginDeferredDiagnosticsForPath(resolvedPath) : undefined,
	);
	invalidateFsScanAfterWrite(resolvedPath);

	const diffResult = generateUnifiedDiffString(result.diffSourceBefore, result.diffSourceAfter);
	const warningsBlock = result.warnings.length > 0 ? `\n\n${result.warnings.join("\n")}` : "";
	const meta = outputMeta()
		.diagnostics(diagnostics?.summary ?? "", diagnostics?.messages ?? [])
		.get();

	return {
		content: [{ type: "text", text: `${result.responseText}${warningsBlock}` }],
		details: {
			diff: diffResult.diff,
			firstChangedLine: diffResult.firstChangedLine,
			diagnostics,
			op: sourceExists ? "update" : "create",
			meta,
		},
	};
}

export async function executeChunkMode(
	options: ExecuteChunkModeOptions,
): Promise<AgentToolResult<EditToolDetails, typeof chunkEditParamsSchema>> {
	const { session, params, signal, batchRequest, writethrough, beginDeferredDiagnosticsForPath } = options;
	const { path, edits } = params;
	const { resolvedPath, sourceFile, sourceExists, rawContent, chunkLanguage } = await resolveChunkSourceContext(
		session,
		path,
	);
	const parentDir = nodePath.dirname(resolvedPath);
	if (parentDir && parentDir !== ".") {
		await fs.mkdir(parentDir, { recursive: true });
	}
	const normalizedOperations = await normalizeChunkEditOperations(edits, {
		resolvedPath,
		sourceExists,
		chunkLanguage,
	});

	const chunkResult = applyChunkEdits({
		source: rawContent,
		language: chunkLanguage,
		cwd: session.cwd,
		filePath: resolvedPath,
		operations: normalizedOperations,
		anchorStyle: resolveAnchorStyle(session.settings),
	});

	if (!chunkResult.changed) {
		const responseText = `[No changes needed — content already matches.]\n\n${chunkResult.responseText}`;
		return {
			content: [{ type: "text", text: responseText }],
			details: {
				diff: "",
				op: sourceExists ? "update" : "create",
				meta: outputMeta().get(),
			},
		};
	}

	return writeChunkResult({
		result: chunkResult,
		resolvedPath,
		sourceFile,
		sourceText: rawContent,
		sourceExists,
		signal,
		batchRequest,
		writethrough,
		beginDeferredDiagnosticsForPath,
	});
}
